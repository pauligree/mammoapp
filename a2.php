            <h3>I pro e contro della mammografia di screening</h3>
            <p>Come ogni intervento medico, la mammografia di screening può dare benefici ma può anche produrre danni.<br />
              Il <strong>vantaggio principale</strong> è la riduzione della mortalità per tumore al seno nelle donne che partecipano al Programma. <br />
              Questo vantaggio è facile da capire ma non va frainteso. La mammografia non evita l’insorgenza del tumore al seno, ma permette di trovare tumori in fase iniziale, per i quali le cure sono meno invasive e più efficaci.</p><p>
              Lo <strong>svantaggio principale</strong> è più difficile da interpretare: consiste nel trattamento inutile, e quindi dannoso (sovratrattamento) di tumori scoperti con lo screening che non si sarebbero mai manifestati (sovradiagnosi). Anche qui non bisogna fraintendere: non si tratta di tumori che a un approfondimento risultano non esistere o non essere maligni, detti “falsi positivi”, ma di tumori maligni che però non sono destinati a crescere o dare metastasi.</p><p>Purtroppo a oggi è impossibile prevedere se un particolare tumore è del tipo innocuo o meno, quindi vengono trattati tutti, e nessuna donna saprà mai quale era il suo caso.<br />
              E’ è però possibile fare una <a href="a3.php">stima delle vite salvate e dei tumori diagnosticati in eccesso</a> per effetto dello screening mammografico.
            </p>
