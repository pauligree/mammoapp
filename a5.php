            <h3>A che età è raccomandato lo Screening mammografico</h3>
              
              <p>In Italia lo screening è organizzato per le donne dai 50 ai 69 anni ogni due anni, mentre in alcune Regioni (Emilia Romagna, Piemonte e Toscana) è esteso alla fascia di età dai 45 ai 74 anni. Dai 45 ai 49 anni è proposto ogni anno.</p>
<p>Istituzioni ed enti accreditati a livello nazionale e internazionale concordano nel dire che:
<ul>
<li>Nelle donne di 40-49 anni le prove che lo screening sia utile, basate sugli studi più recenti, sono considerate limitate, soprattutto per il rapporto tra i possibili benefici e i danni dello screening (come <a href="b2.php">falsi positivi</a> e <a href="d3.php">sovradiagnosi</a>).</li>
<li>Nelle donne di 50-69 anni lo screening è raccomandato perché le prove che riduca la mortalità per tumore al seno sono considerate convincenti.</li>
<li>Nelle donne di 70-74 anni lo screening è raccomandato perché le prove (basate su studi diversi dai precedenti) sono considerate sufficienti.</li>
</ul></p>
