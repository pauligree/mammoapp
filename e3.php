          <p><strong>Indice di massa corporea</strong> (IMC o BMI, Body Mass Index) Si calcola dividendo il peso corporeo (in kg) per la statura (in metri) elevata al quadrato e si usa per stabilire il sovrappeso (&ge;25 kg/m&sup2;) o l&rsquo;obesit&agrave; (&ge;30 kg/m&sup2;).</p>
<p><strong>Dieta mediterranea</strong> Si tratta, pi&ugrave; che di uno specifico programma dietetico, di un modello nutrizionale equilibrato che si rif&agrave; alle abitudini alimentari seguite dai popoli della regione mediterranea. E&rsquo; caratterizzata dall&rsquo;assunzione di frutta, verdura, cereali integrali, pesce, legumi, noci, olio d&rsquo;oliva e modiche quantit&agrave; di vino.</p>
<p><strong>Dieta vegana</strong> Modello nutrizionale che esclude tutti gli alimenti di origine animale, compresi uova e latte, a favore di alimenti provenienti esclusivamente dal regno vegetale. Data la estrema selettivit&agrave; rispetto ad altri modelli vegetariani, non &egrave; bilanciata e comporta il rischio di carenze di vitamine (come la vitamina B12) e di sali minerali (come il ferro).</p>
<p><strong>Dose di alcol</strong> Il consumo di alcol viene abitualmente espresso facendo riferimento all&rsquo;unit&agrave; alcolica o &ldquo;bicchier standard&rdquo; che &egrave; uguale a circa 10-12 grammi di alcol puro e corrisponde a un bicchiere di vino (12&deg;, 125 ml), una lattina di birra (4,5&deg;, 330 ml), un aperitivo (18&deg;, 80 ml), un bicchierino di superalcolico</p>
<p><strong>Sindrome metabolica</strong> Condizione patologica caratterizzata dalla presenza di almeno 3 tra le seguenti alterazioni:</p>
<ul>
<li>accumulo di grasso a livello dell&rsquo;addome</li>
<li>obesit&agrave;</li>
<li>aumento dei trigliceridi nel sangue</li>
<li>riduzione del colesterolo HDL nel sangue</li>
<li>aumento della pressione arteriosa</li>
<li>aumento della glicemia fino alla presenza di diabete di tipo 2 (diabete non insulino-dipendente)..</li>
</ul>
<p>Le persone con sindrome metabolica hanno un aumento del rischio di malattie cardiovascolari e di alcune forme tumorali.</p>
<p><strong>Studio osservazionale</strong> Tipo di studio in cui i ricercatori si limitano a osservare gli eventi nei gruppi allo studio senza intervenire direttamente. Per esempio, nel caso dello screening mammografico, i ricercatori seguono nell&rsquo;arco del tempo (per anni) donne che hanno partecipato e non hanno partecipato allo screening e rilevano la frequenza di diversi eventi (l&rsquo;insorgenza del tumore al seno, di morti per la malattia, di sovradiagnosi, eccetera).</p>
<p><strong>Studio sperimentale</strong> Tipo di studio in cui i ricercatori valutano l&rsquo;efficacia di un intervento (un trattamento o un esame utile per la diagnosi) nel modificare la frequenza degli eventi che si vogliono valutare (i risultati). L&rsquo;intervento &egrave; proposto in maniera casuale alla met&agrave; di un certo numero di persone con caratteristiche simili selezionate in partenza. Per esempio, nel caso dello screening mammografico, i ricercatori assegnano un certo numero di donne al gruppo di intervento (partecipanti allo screening) o al gruppo di controllo (non partecipanti allo screening) e rilevano al termine di un periodo di osservazione la frequenza di insorgenza del tumore al seno, di morti per la malattia, di sovradiagnosi, eccetera).</p>
<p><strong>Revisione di studi</strong> Le revisioni sono studi che combinano e riassumono i risultati di altri studi in modo da fornire un&rsquo;unica stima complessiva, e pi&ugrave; affidabile, dei dati disponibili. Una revisione viene detta sistematica se viene fatta secondo un metodo predefinito (l&rsquo;esempio tipico sono le revisioni della Cochrane Collaboration).</p>
<p><strong>Aumento o riduzione relativa del rischio</strong> <br />Indica di quanto varia il rischio, per esempio di ammalarsi di tumore al seno, in funzione di un fattore variabile, come un certo tipo di alimentazione o l&rsquo;essere in sovrappeso durante la menopausa.</p>
<p><strong>Terapia ormonale sostitutiva (TOS o HRT, Hormone Replacement Therapy)</strong> Somministrazione di ormoni femminili (estrogeni e/o progestinici) quando, con la menopausa o per un intervento chirurgico, &egrave; cessata la funzione ovarica.</p>
