						<h2>Opuscolo</h2>
						<p>Lo screening è un&rsquo;azione di sanità pubblica con la quale la popolazione sana, in età giudicata a rischio, è invitata dalla propria azienda sanitaria a sottoporsi gratuitamente ad esami preventivi (quali mammografia, pap-test e ricerca del sangue occulto fecale).<br /> Lo screening può permettere di anticipare la diagnosi e la cura del tumore, consentendo in genere interventi chirurgici più semplici. Per lo screening del tumore della mammella ogni donna tra i 50 e i 69 anni riceve una lettera di invito con appuntamento prefissato per effettuare una mammografia gratuita ogni 2 anni. La ricerca scientifica ha dimostrato che gli screening per i tumori della mammella, dell&rsquo;utero e del colon-retto sono in grado di salvare numerose vite umane.</p>
						<h3>La Mammografia</h3>
						<p>Attualmente, per le donne tra i 50 e i 69 anni, la mammografia è il test più efficace per individuare i tumori che non danno ancora sintomi e non sono palpabili.<br /> È una radiografia del seno che utilizza dosi di raggi X molto basse. Per ottenere un risultato chiaro è necessario comprimere le mammelle. Questa compressione può risultare fastidiosa o leggermente dolorosa, ma dura pochi secondi. Il personale che effettua la mammografia è altamente qualificato e le apparecchiature utilizzate sono tecnologicamente avanzate e controllate. Tutto ciò permette di ottenere immagini di ottima qualità.</p>
						<p>Per una maggiore sicurezza sulla diagnosi, le immagini sono esaminate separatamente da due radiologi per garantire una maggiore precisione. Tuttavia, come tutti gli esami, non è infallibile: in alcuni casi può non riconoscere un tumore che c&rsquo;è o fare sospettare un tumore inesistente.</p>
						<h3>Perche&rsquo; Fare La Mammografia</h3>
						<p>In Italia i tumori della mammella sono al primo posto tra le donne per incidenza, cioè per nuovi casi l'anno. E&rsquo; il tumore più frequente fra le donne, con 36.000 nuovi casi e 11.000 decessi all&rsquo;anno. La mammografia eseguita regolarmente permette di effettuare una diagnosi precoce, prima dell'insorgenza dei sintomi, e di poter intervenire sul tumore con elevate probabilità di guarigione definitiva. I dati indicano che, per ogni 1000 donne di età tra i 50 e i 69 anni che eseguono regolarmente la mammografia, nell&rsquo;arco di 20 anni si possono salvare 7-9 vite.</p>
						<h3>Quali Sono I Limiti/svantaggi Della Mammografia?</h3>
						<ul>
							<li>In genere ogni 100 donne che effettuano una mammografia, circa 5 devono ripetere l&rsquo;esame o sottoporsi ad approfondimenti. Per 4 di loro si tratta in realtà di &lsquo;falsi allarmi&rsquo;, in quanto gli approfondimenti escludono la presenza di un tumore (i cosiddetti falsi positivi). Lo svantaggio è rappresentato principalmente dall'ansia che una donna può provare in tali situazioni.</li>
							<li>Non sempre la mammografia è in grado di evidenziare il tumore quando è presente. In alcuni casi la mammella è molto densa (cioè contiene molte ghiandole) e ciò rende difficile l&rsquo;identificazione del tumore (i cosiddetti falsi negativi).</li>
							<li>Ci sono casi in cui la malattia si sviluppa molto rapidamente nell&rsquo;intervallo tra due test di screening (i cosiddetti cancri di intervallo). È quindi importante prestare sempre attenzione ai cambiamenti del seno come indurimenti, cambiamenti della pelle o fuoriuscita di liquido dal capezzolo e alla presenza di noduli ascellari, anche nel caso in cui la mammografia non abbia evidenziato anomalie. Se si notano delle anomalie è opportuno rivolgersi subito al proprio medico.</li>
							<li>Alcune anomalie, tra quelle che vengono rilevate dalla mammografia, non sono destinate a trasformarsi in tumori invasivi e a compromettere la salute della donna (la cosiddetta sovradiagnosi). In questi casi (1 caso di tumore ogni 10 diagnosticati) si può incorrere in interventi che non sarebbero stati necessari. Questo purtroppo è inevitabile in quanto le conoscenze attuali non consentono di distinguere fin dall&rsquo;inizio tra i tumori che sono una minaccia per la vita e quelli che non lo sono.</li>
						</ul>
						<h3>Quale Può Essere L&rsquo;esito Della Mammografia?</h3>
						<ul>
							<li>Se non risulta nulla di sospetto, riceverà una lettera di esito negativo.<br /> È bene sapere che lo scopo dello screening mammografico è individuare tumori maligni riconoscibili ai raggi x; non vengono considerate altre patologie della mammella.</li>
							<li>Se si evidenziano immagini dubbie, sarà contattata ed invitata a recarsi al centro di screening per eseguire approfondimenti, che possono comprendere altre mammografie, ecografia o talvolta un piccolo prelievo di tessuto mammario (biopsia). La maggior parte di questi casi dubbi non è dovuta alla presenza di un tumore, ma è necessario eseguire questi esami supplementari proprio per esserne sicuri.</li>
						</ul>


